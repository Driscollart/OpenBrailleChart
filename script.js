function ViewModel() {
    var self = this;
    self.alphabet = alphabet;
    self.numbers = numbers;
    self.punctuation = punctuation;

    self.getCell = function (cell) {
        if (cell === true) {
            return "•"
        } else {
            return "◦";
        }
    }

    self.getNumbers = function (index, type) {
        var numbers = [];
        var currentItem = null;
        switch (type) {
            case 'alphabet':
                currentItem = self.alphabet[index];
                break;
            case 'numbers':
                currentItem = self.numbers[index];
                break;
            case 'punctuation':
                currentItem = self.punctuation[index];
                break;
        }

        if (currentItem.cell1 === true) {
            numbers.push('1');
        }
        if (currentItem.cell2 === true) {
            numbers.push('2');
        }
        if (currentItem.cell3 === true) {
            numbers.push('3');
        }
        if (currentItem.cell4 === true) {
            numbers.push('4');
        }
        if (currentItem.cell5 === true) {
            numbers.push('5');
        }

        if (currentItem.cell6 === true) {
            numbers.push('6');
        }

        return numbers.join(', ');
    }
};

ko.applyBindings(new ViewModel());